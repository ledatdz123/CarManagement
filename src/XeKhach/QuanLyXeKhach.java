    private void btnSuaChuyenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaChuyenActionPerformed
        // TODO add your handling code here:
        if (btnSuaChuyen.getText().toString().equals("SỬA CHUYẾN")){
            btnThemChuyen.setText("THÊM CHUYẾN");
            btnSuaChuyen.setText("XÁC NHẬN SỬA");
            CHUYENXE.loadDSChuyen(tbDSChuyen);
        } else if (btnSuaChuyen.getText().toString().equals("XÁC NHẬN SỬA")) {
            if (ClickTbDSChuyen()) {
                Date date = new Date();
                String maChuyen = txtMaChuyen.getText();
                String ngayDi = format.format(dateNgayDi.getDate());
                int maXe = Integer.parseInt(cbXE.getSelectedItem().toString());
                String maLT = cbMaLT.getSelectedItem().toString();
                Date date_ngayDi;
                try {
                    date_ngayDi = format.parse(ngayDi);
                    if (date_ngayDi.after(date)) {

                        CHUYENXE cx = new CHUYENXE();
                        cx.setMaCX(maChuyen);
                        cx.setNgayDi(ngayDi);
                        cx.setMaXe(maXe);
                        cx.setMaLT(maLT);
                        CHUYENXE.UpdateTour(cx, maChuyen);
                        CHUYENXE.loadDSChuyen(tbDSChuyen);
                    } else {
                        JOptionPane.showMessageDialog(this, "Chuyến xe này đã hoàn thành, không sửa được");
                    }
                } catch (ParseException ex) {
                    Logger.getLogger(QuanLyXeKhach.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnSuaChuyenActionPerformed

